# Faça um Programa que peça as 4 notas bimestrais e mostre a média.
nota_1 = int(input('Insira o valor da primeira nota: '))
nota_2 = int(input('Insira o valor da primeira nota: '))
nota_3 = int(input('Insira o valor da primeira nota: '))
nota_4 = int(input('Insira o valor da primeira nota: '))
resultado = nota_1 + nota_2 + nota_3 + nota_4
media = resultado / 4
print(f'A média das notas informadas é {media}')
